def atomic_temp_path(path: Path) -> Path:
    return path.with_name(f".{path.name}.{os.getpid()}.tmp")


def atomic_write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = atomic_temp_path(path)
    try:
        tmp_path.write_text(text, encoding="utf-8")
        tmp_path.replace(path)
    finally:
        if tmp_path.exists():
            tmp_path.unlink()


def atomic_write_csv(path: Path, rows: list[dict[str, object]], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = atomic_temp_path(path)
    try:
        with tmp_path.open("w", newline="", encoding="utf-8") as handle:
            writer = csv.DictWriter(handle, fieldnames=fieldnames, extrasaction="ignore")
            writer.writeheader()
            for row in rows:
                writer.writerow({field: row.get(field, "") for field in fieldnames})
        tmp_path.replace(path)
    finally:
        if tmp_path.exists():
            tmp_path.unlink()


def final_figure_folders() -> tuple[Path, ...]:
    return (MAIN_FIGURES_DIR, SCENARIO_FIGURES_DIR, DIAGNOSTIC_FIGURES_DIR, FIGURES_DIR)


def clear_generated_final_evaluation_images() -> list[Path]:
    """Remove only generated final-evaluation image files before a clean render."""
    deleted: list[Path] = []
    for folder in final_figure_folders():
        folder.mkdir(parents=True, exist_ok=True)
        for path in sorted(folder.iterdir()):
            if path.is_file() and path.suffix.lower() in GENERATED_IMAGE_EXTENSIONS:
                path.unlink()
                deleted.append(path)
    return deleted


