def parse_feature_classification(text: str) -> dict[str, int]:
    counts: Counter[str] = Counter()
    for line in text.splitlines():
        if "classification=" not in line:
            continue
        segment = line.split("classification=", 1)[1]
        classification = segment.split(";", 1)[0].strip()
        if classification:
            counts[classification] += 1
    return dict(sorted(counts.items()))


