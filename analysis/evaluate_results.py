def bfd_like_delay_diagnostic_rows(final_rows: list[dict[str, object]]) -> list[dict[str, object]]:
    rows: list[dict[str, object]] = []
    for row in final_rows:
        if row.get("mechanism_family") != "bfd_like_frr":
            continue
        delay = parse_float(row.get("average_udp_delay_ms"))
        post_gap = parse_float(row.get("receiver_observed_gap_count_mean"))
        recovery = parse_float(row.get("recovery_time_after_failure_s"))
        reordered = parse_float(row.get("activation_to_failure_reordered_mean"))
        lead = parse_float(row.get("lead_time_mean_s"))
        if (post_gap or 0.0) > 0.0 and lead is None:
            explanation = "Reactive comparator did not provide useful pre-failure protection in this profile; delay is over delivered packets and must be read with remaining gaps."
            suspicious = "no"
        elif (reordered or 0.0) > 0.0 or (recovery or 0.0) > 0.0:
            explanation = "High delay is consistent with reactive transition timing, failover/reordering, or receiver-observed recovery effects."
            suspicious = "no"
        elif delay is not None and delay > 10.0:
            explanation = "Delay is elevated without an obvious gap/reordering/recovery explanation in the summary row; inspect raw vectors before using as a headline."
            suspicious = "yes"
        else:
            explanation = "No unexplained high-delay symptom in the aggregate row."
            suspicious = "no"
        rows.append(
            {
                "scenario": row.get("scenario", ""),
                "profile": row.get("profile", ""),
                "activation_time_mean": row.get("activation_time_mean_s", ""),
                "lead_time_mean": row.get("lead_time_mean_s", ""),
                "post_failure_unobserved_mean": row.get("receiver_observed_gap_count_mean", ""),
                "udp_delay_mean_ms": row.get("average_udp_delay_ms", ""),
                "recovery_time_after_failure_s": row.get("recovery_time_after_failure_s", ""),
                "activation_to_failure_reordered_mean": row.get("activation_to_failure_reordered_mean", ""),
                "likely_explanation": explanation,
                "suspicious_yes_no": suspicious,
            }
        )
    return rows


