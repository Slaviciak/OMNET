def scenario_profile_family_points(
    final_rows: list[dict[str, object]],
    fields: tuple[str, ...],
) -> list[dict[str, object]]:
    groups: dict[tuple[str, str, str], dict[str, list[float]]] = defaultdict(lambda: {field: [] for field in fields})
    for row in final_rows:
        key = (
            str(row.get("scenario", "")),
            str(row.get("profile", "") or "core_reference"),
            str(row.get("protection_family", "")),
        )
        if not all(key):
            continue
        for field in fields:
            value = parse_float(row.get(field))
            if value is not None:
                groups[key][field].append(value)
    points: list[dict[str, object]] = []
    for (scenario, profile, family), values_by_field in groups.items():
        point = {"scenario": scenario, "profile": profile, "protection_family": family}
        for field in fields:
            values = values_by_field.get(field, [])
            point[field] = fmean(values) if values else None
        points.append(point)
    return sorted(
        points,
        key=lambda row: (
            SCENARIO_ORDER.get(str(row.get("scenario", "")), 99),
            PROFILE_ORDER.get(str(row.get("profile", "")), 99),
            PROTECTION_FAMILY_ORDER.get(str(row.get("protection_family", "")), 99),
        ),
    )


