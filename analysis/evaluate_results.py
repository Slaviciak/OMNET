def scalar_value_from_line(line: str) -> tuple[str, str, float] | None:
    parts = line.strip().split()
    if len(parts) < 4 or parts[0] != "scalar":
        return None
    try:
        return parts[1], parts[2], float(parts[3])
    except ValueError:
        return None


def raw_scalar_path_for_outcome(scenario: str, row: dict[str, str]) -> Path | None:
    config_name = row.get("config_name", "").strip()
    run_number = row.get("run_number", "").strip()
    if not config_name or run_number == "":
        return None
    result_dir = scenario_result_dir(scenario)
    candidates = [
        result_dir / f"{config_name}Cohort-{run_number}.sca",
        result_dir / f"{config_name}-{run_number}.sca",
    ]
    for candidate in candidates:
        if candidate.exists():
            return candidate
    matches = sorted(result_dir.glob(f"{config_name}*Cohort-{run_number}.sca"))
    if matches:
        return matches[0]
    matches = sorted(result_dir.glob(f"{config_name}*-{run_number}.sca"))
    return matches[0] if matches else None


def parse_monitored_udp_counts(sca_path: Path) -> dict[str, float | str | None]:
    sent = None
    received = None
    if not sca_path or not sca_path.is_file():
        return {
            "udp_packets_sent": None,
            "udp_packets_received": None,
            "udp_packet_loss_count": None,
            "udp_packet_loss_percent": None,
            "udp_loss_source": "missing_sca",
        }
    with sca_path.open("r", encoding="utf-8", errors="replace") as handle:
        for line in handle:
            parsed = scalar_value_from_line(line)
            if parsed is None:
                continue
            module, scalar, value = parsed
            if module == "RegionalBackbone.hostA.app[0]" and scalar == "packetSent:count":
                sent = value
            elif module == "RegionalBackbone.hostB.app[0]" and scalar == "packetReceived:count":
                received = value
            if sent is not None and received is not None:
                break
    loss = None
    loss_percent = None
    if sent is not None and received is not None:
        loss = max(0.0, sent - received)
        loss_percent = (loss / sent * 100.0) if sent > 0 else 0.0
    return {
        "udp_packets_sent": sent,
        "udp_packets_received": received,
        "udp_packet_loss_count": loss,
        "udp_packet_loss_percent": loss_percent,
        "udp_loss_source": "exact_app0_sent_received_scalars" if loss is not None else "incomplete_app0_scalars",
    }


