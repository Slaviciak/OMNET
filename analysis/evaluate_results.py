def summarize_numeric(rows: list[dict[str, str]], field: str) -> tuple[str, str, str]:
    values = [value for value in (parse_float(row.get(field)) for row in rows) if value is not None]
    if not values:
        return "", "", ""
    return fmt(min(values)), fmt(max(values)), fmt(fmean(values))


def create_shallow_tree_activation_audit(
    outcome_rows: list[dict[str, str]],
    trace_rows: list[dict[str, str]],
    runtime_tree_rows: list[dict[str, str]],
    offline_ml_rows: list[dict[str, str]],
    offline_timing_rows: list[dict[str, str]],
) -> tuple[dict[str, object], str]:
    shallow_outcomes = [row for row in outcome_rows if row.get("protection_mode") == "aimrce_shallow_tree_frr"]
    loaded_values = {row.get("runtime_model_loaded", "") for row in shallow_outcomes}
    fallback_values = {row.get("runtime_model_fallback_used", "") for row in shallow_outcomes}
    activation_count = sum(1 for row in shallow_outcomes if str(row.get("protection_activated", "")).strip() in {"1", "1.0", "true", "True"})
    fallback_count = sum(1 for row in shallow_outcomes if str(row.get("runtime_model_fallback_used", "")).strip() in {"1", "1.0", "true", "True"})
    run_count = len({(row.get("degradation_profile", ""), row.get("run_number", "")) for row in shallow_outcomes})
    post_unobserved_by_profile: dict[str, list[float]] = defaultdict(list)
    for row in shallow_outcomes:
        value = parse_float(row.get("packet_sequence_gap_total_unobserved_after_hard_failure"))
        if value is not None:
            post_unobserved_by_profile[row.get("degradation_profile", "")].append(value)

    feature_rows = [row for row in runtime_tree_rows if row.get("row_type") == "feature"]
    feature_names = [row.get("name", "") for row in feature_rows if row.get("name")]
    node_rows = [row for row in runtime_tree_rows if row.get("row_type") == "node"]
    root = node_rows[0] if node_rows else {}
    root_feature = root.get("name", "")
    root_threshold = root.get("threshold", "")

    shallow_trace = [row for row in trace_rows if row.get("mechanism") == "aimrce_shallow_tree_frr"]
    risk_min, risk_max, risk_mean = summarize_numeric(shallow_trace, "risk_score")
    queue_min, queue_max, queue_mean = summarize_numeric(shallow_trace, "queue_length")
    delay_min, delay_max, delay_mean = summarize_numeric(shallow_trace, "receiver_delay")
    pkt_min, pkt_max, pkt_mean = summarize_numeric(shallow_trace, "packet_count")
    positive_trace_count = sum(1 for row in shallow_trace if (parse_float(row.get("decision_positive")) or 0.0) > 0.0)

    offline_tree_rows = [row for row in offline_ml_rows if row.get("model") == "shallow_tree"]
    offline_timing_tree_rows = [row for row in offline_timing_rows if row.get("model") == "shallow_tree"]
    offline_summary = []
    for row in offline_tree_rows:
        offline_summary.append(
            f"{row.get('feature_set')} F1={fmt(parse_float(row.get('f1_mean')), 4)} precision={fmt(parse_float(row.get('precision_mean')), 4)} recall={fmt(parse_float(row.get('recall_mean')), 4)}"
        )
    timing_numbers = sorted({
        value
        for row in offline_timing_tree_rows
        if (value := parse_float(row.get("offline_activation_time_s"))) is not None
    })
    timing_values = [fmt(value, 3) for value in timing_numbers]

    no_bug = (
        run_count > 0
        and loaded_values <= {"1", "1.0"}
        and fallback_count == 0
        and activation_count == 0
        and root_feature == "bottleneck_queue_length_last_pk"
        and parse_float(queue_max) is not None
        and (parse_float(queue_max) or 0.0) <= (parse_float(root_threshold) or 0.0)
    )
    conclusion = (
        "NO BUG FOUND: the deployed shallow-tree artifact loads successfully and classifies the cost-aware traced windows as negative because the root split uses bottleneck_queue_length_last_pk <= 20 and traced queue_length remains at 0."
        if no_bug
        else "INCONCLUSIVE: inspect runtime artifact, trace coverage, and feature ranges before changing model artifacts."
    )

    profile_lines = []
    for profile, values in sorted(post_unobserved_by_profile.items(), key=lambda item: PROFILE_ORDER.get(item[0], 99)):
        profile_lines.append(f"- {profile}: mean post-failure unobserved gaps {fmt(fmean(values))} across {len(values)} runs")

    text = "\n".join(
        [
            "Shallow-tree cost-aware activation audit",
            "========================================",
            "",
            "Scope:",
            f"- Scenario: {COST_AWARE_SCENARIO}",
            "- Mechanism: aimrce_shallow_tree_frr",
            "- This audit is reporting-only and does not change runtime artifacts or simulator behavior.",
            "",
            "Artifact load and fallback:",
            f"- Outcome rows inspected: {len(shallow_outcomes)}",
            f"- Profile/run combinations inspected: {run_count}",
            f"- runtime_model_loaded values: {sorted(loaded_values)}",
            f"- runtime_model_fallback_used values: {sorted(fallback_values)}",
            f"- fallback count: {fallback_count}",
            f"- protection activation count: {activation_count}",
            "",
            "Runtime artifact:",
            f"- Runtime tree features: {', '.join(feature_names)}",
            f"- Root split: {root_feature} <= {root_threshold} goes to the negative leaf; values above the split enter positive leaves in this exported artifact.",
            "- Feature names match AiMrceController::lookupFeatureValue for the four deployed runtime features.",
            "",
            "Cost-aware trace evidence:",
            f"- Shallow-tree trace rows inspected: {len(shallow_trace)}",
            f"- risk_score min/max/mean: {risk_min}/{risk_max}/{risk_mean}",
            f"- queue_length min/max/mean: {queue_min}/{queue_max}/{queue_mean}",
            f"- receiver_delay min/max/mean: {delay_min}/{delay_max}/{delay_mean}",
            f"- packet_count min/max/mean: {pkt_min}/{pkt_max}/{pkt_mean}",
            f"- positive decision trace rows: {positive_trace_count}",
            "",
            "Outcome evidence by profile:",
            *(profile_lines or ["- No profile-level shallow-tree outcome rows found."]),
            "",
            "Offline audit comparison:",
            *(f"- {line}" for line in offline_summary),
            f"- Offline shallow-tree activation-time samples include: {', '.join(timing_values) if timing_values else 'none available'}",
            "- The offline audit trains/evaluates feasibility models on offline data and is not the deployed runtime tree artifact.",
            "",
            "Conclusion:",
            f"- {conclusion}",
            "",
            "Recommended action:",
            "- Do not modify C++, thresholds, repair routes, or deployed runtime CSVs for this finding.",
            "- Document shallow tree as conservative/not robust under the cost-aware profile because its deployed tree relies on queue buildup that is absent in the traced cost-aware activation windows.",
            "- If shallow-tree runtime behavior must be improved, handle that later as an explicit runtime-model-v2 retraining/export task with leakage review and full regression validation.",
            "",
        ]
    )

    summary = {
        "conclusion": conclusion,
        "rows": len(shallow_outcomes),
        "run_count": run_count,
        "loaded_values": ",".join(sorted(loaded_values)),
        "fallback_count": fallback_count,
        "activation_count": activation_count,
        "root_feature": root_feature,
        "root_threshold": root_threshold,
        "queue_max": queue_max,
        "risk_max": risk_max,
    }
    return summary, text


def plot_feature_importance(plt, importance_rows: list[dict[str, object]], output_path: Path) -> bool:
    if not importance_rows:
        return False
