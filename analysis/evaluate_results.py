def metric_group_from_filename(filename: str) -> str:
    name = filename.lower()
    if "packet_loss" in name or "gap_percent" in name:
        return "udp_packet_loss_or_continuity"
    if "tradeoff" in name or "trade_off" in name:
        return "delivery_recovery_tradeoff"
    if "udp_delay" in name or "delay_queueing" in name:
        return "udp_delay_qos"
    if "recovery" in name:
        return "recovery_time_proxy"
    if "reordering" in name:
        return "transition_reordering"
    if "lead_time" in name or "leadtime" in name or "activation" in name:
        return "activation_lead_time"
    if "tcp" in name:
        return "tcp_endpoint_proxy"
    if "feature" in name or "driver" in name:
        return "ml_feature_driver"
    if "summary" in name or "stability" in name:
        return "network_summary"
    return "diagnostic"


