def feature_category(feature: str, group: str = "") -> str:
    text = f"{feature} {group}".lower()
    if "queue" in text:
        return "queue_congestion"
    if "delay" in text or "jitter" in text:
        return "delay_qos"
    if "throughput" in text or "goodput" in text:
        return "throughput_goodput"
    if "packet_count" in text or "continuity" in text or "interarrival" in text or "unobserved" in text:
        return "packet_count_continuity"
    if "bfd" in text or "probe" in text:
        return "bfd_diagnostic"
    if "per" in text or "error" in text or "impairment" in text:
        return "impairment_context"
    if "risk" in text or "streak" in text or "protection" in text:
        return "controller_state"
    return "other_runtime_telemetry"


def feature_interpretation(feature: str, category: str) -> str:
    if category == "queue_congestion":
        return "Queue/congestion pressure is a plausible precursor and network-stability driver."
    if category == "delay_qos":
        return "Receiver delay is a direct UDP/QoS symptom and a plausible runtime signal."
    if category == "throughput_goodput":
        return "Throughput/goodput proxy reflects application delivery rate changes."
    if category == "packet_count_continuity":
        return "Packet-count or continuity telemetry captures delivery progress, but must avoid label leakage."
    if category == "bfd_diagnostic":
        return "BFD-like diagnostics are comparator/control-state signals; use cautiously for runtime ML inputs."
    if category == "impairment_context":
        return "Impairment context is useful for analysis but can be simulator-only or leakage-prone."
    if category == "controller_state":
        return "Controller-state fields are diagnostic and should not train a pre-activation decision policy."
    return "Runtime telemetry candidate; inspect provenance and leakage classification before deployment."


def model_feature_importance_summary(rows_by_scenario: dict[str, list[dict[str, str]]], limit_per_model: int = 10) -> list[dict[str, object]]:
    grouped: dict[tuple[str, str, str], list[dict[str, str]]] = defaultdict(list)
    for scenario, rows in rows_by_scenario.items():
        for row in rows:
            if not row.get("feature"):
                continue
            grouped[(scenario, row.get("feature_set", ""), row.get("model", ""))].append(row)

    output_rows: list[dict[str, object]] = []
    for (scenario, feature_set, model), items in sorted(grouped.items()):
        ranked = sorted(items, key=lambda row: parse_float(row.get("abs_value")) or 0.0, reverse=True)
        for rank, row in enumerate(ranked[:limit_per_model], start=1):
            feature = row.get("feature", "")
            category = feature_category(feature, row.get("group", ""))
            classification = row.get("classification", "")
            output_rows.append(
                {
                    "scenario": scenario,
                    "feature_set": feature_set,
                    "model": model,
                    "feature": feature,
                    "importance_or_coefficient": row.get("value") or row.get("abs_value", ""),
                    "rank": rank,
                    "feature_category": category,
                    "interpretation": feature_interpretation(feature, category),
                    "runtime_safe_candidate": "yes" if "runtime-safe" in classification.lower() else "no",
                }
            )
    return output_rows


