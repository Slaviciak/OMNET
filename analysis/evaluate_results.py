def plot_grouped_by_profile(
    plt,
    rows: list[dict[str, object]],
    field: str,
    title: str,
    ylabel: str,
    output_path: Path,
) -> bool:
    candidate_profiles = ("mild_slow", "moderate", "severe_fast", "cost_aware_mild", "cost_aware_moderate", "cost_aware_fast_warning")
    profiles = [profile for profile in candidate_profiles if any(row.get("profile") == profile and numeric_from_row(row, field) is not None for row in rows)]
    mechanisms = [mechanism for mechanism in MECHANISM_ORDER if any(row.get("mechanism_family") == mechanism and numeric_from_row(row, field) is not None for row in rows)]
    if not profiles or not mechanisms:
        return False

    width = 0.11 if len(mechanisms) > 5 else 0.16
    x_positions = list(range(len(profiles)))
    plt.figure(figsize=(12, 5.8))
    for index, mechanism in enumerate(mechanisms):
        values: list[float] = []
        for profile in profiles:
            match = next((row for row in rows if row.get("profile") == profile and row.get("mechanism_family") == mechanism), None)
            value = numeric_from_row(match or {}, field)
            values.append(value if value is not None else 0.0)
        offsets = [x + (index - (len(mechanisms) - 1) / 2) * width for x in x_positions]
        plt.bar(offsets, values, width=width, label=PLOT_LABELS.get(mechanism, mechanism))
    plt.title(title, pad=10)
    plt.ylabel(ylabel)
    plt.xticks(x_positions, [profile_label(profile) for profile in profiles])
    plt.grid(axis="y", alpha=0.3)
    plt.legend(fontsize=8, ncols=2, loc="best")
    plt.tight_layout()
    output_path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(output_path, dpi=180)
    plt.close()
    return True


